public class ComparisonOp {
    public static void main(String[] args) {
        int khalidAge = 20;
        int mariamAge = 20;

        System.out.println(khalidAge > mariamAge);
        System.out.println(khalidAge < mariamAge);
        System.out.println(khalidAge == mariamAge);
        System.out.println(khalidAge >= mariamAge);
        System.out.println(khalidAge <= mariamAge);
        System.out.println(khalidAge != mariamAge);

    }
}
